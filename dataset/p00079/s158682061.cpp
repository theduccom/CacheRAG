using namespace std;

double area(double a,double b,double c){
  double z=(a+b+c)/2.0;
  return sqrt(z*(z-a)*(z-b)*(z-c));
}

double len(double x1,double x2,double y1,double y2){
  return sqrt((x2-x1)*(x2-x1)+(y2-y1)*(y2-y1));
}
  

int main(){
  double x[21],y[21];
  double s=0.0;
  int n=0;
  while(true){
    vector<string> v;
    string s;
    cin >>s;
    if(!cin) break;
    else{
      stringstream ss;
      ss << s;
      string buf;
      while(getline(ss,buf,',')){
	v.push_back(buf);
      }
      istringstream xs(v[0]);
      istringstream ys(v[1]);

      xs>>x[n];
      ys>>y[n];
      n++;
    }
  }
  n-=1;
  for(int i=1;i<n;i++){
    double a,b,c;
    a=len(x[0],x[i],y[0],y[i]);
    b=len(x[i],x[i+1],y[i],y[i+1]);
    c=len(x[i+1],x[0],y[i+1],y[0]);;
    s+=area(a,b,c);
  }
  printf("%.6f\n",s);
}