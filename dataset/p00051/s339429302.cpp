#include<iostream>
#include<algorithm>
using namespace std; 

int main(){
	int n;
	cin >> n;
	
	for(int i=0;i<n;i++){
		int k;
		cin >> k;
		int a[8];
		a[0]=k/10000000;
		a[1]=k/1000000-a[0]*10;
		a[2]=k/100000-a[0]*100-a[1]*10;
		a[3]=k/10000-a[0]*1000-a[1]*100-a[2]*10;
		a[4]=k/1000-a[0]*10000-a[1]*1000-a[2]*100-a[3]*10;
		a[5]=k/100-a[0]*100000-a[1]*10000-a[2]*1000-a[3]*100-a[4]*10;
		a[6]=k/10-a[0]*1000000-a[1]*100000-a[2]*10000-a[3]*1000-a[4]*100-a[5]*10;
		a[7]=k-a[0]*10000000-a[1]*1000000-a[2]*100000-a[3]*10000-a[4]*1000-a[5]*100-a[6]*10;
		sort(a,a+8);
		int b,c;
		b=a[7]*10000000+a[6]*1000000+a[5]*100000+a[4]*10000+a[3]*1000+a[2]*100+a[1]*10+a[0];
		c=a[0]*10000000+a[1]*1000000+a[2]*100000+a[3]*10000+a[4]*1000+a[5]*100+a[6]*10+a[7];
		cout << b-c << endl;
	}
	return 0;
}