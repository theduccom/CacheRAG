#include <iostream>
#include <string>
using namespace std;

string s;

int single(int& pos);

int figure(int& pos);

int solve(int& pos);

int main(){
	int T;
	cin>>T;
	for(int i = 0; i < T; i++){
		cin>>s;
		int pos = 0;
		cout<<solve(pos)<<endl;
	}
	
	return 0;
}

int single(int& pos){//??°??????????????????????????¢??°
	if(s[pos]!='('){
		int res = 0;
		while('0'<=s[pos] && s[pos]<='9'){
			res *= 10;
			res += s[pos]-48;
			pos++;
		}
		return res;
	}else{
		return solve(++pos);
	}
}

int figure(int& pos){//????????????????????????????????¢??°
	int res = single(pos);
	while(1){
		if(s[pos]=='*'){
			res *= single(++pos);
		}else if(s[pos]=='/'){
			res /= single(++pos);
		}else {
			return res;
		}
	}
}

int solve(int& pos){//?????¨???????¨????????????¢??°(????????????????¨?????????????????????¨?????????)
	int res = figure(pos);
	while(s[pos]!='=' && s[pos]!=')'){
		if(s[pos]=='+') res += figure(++pos);
		else if(s[pos]=='-') res -= figure(++pos);
	}
	pos++;
	return res;
}