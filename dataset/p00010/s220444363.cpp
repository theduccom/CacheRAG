#include <math.h>
int main(){
    double x[4],y[4];
    int n;
    scanf("%d",&n);
    for(int i=0;i<n;i++){
        for(int i=0;i<3;i++)scanf("%lf%lf",&x[i],&y[i]);
        x[3]=y[0]*(x[2]*x[2]+y[2]*y[2])+y[1]*(x[0]*x[0]+y[0]*y[0])+y[2]*(x[1]*x[1]+y[1]*y[1])-y[0]*(x[1]*x[1]+y[1]*y[1])-y[1]*(x[2]*x[2]+y[2]*y[2])-y[2]*(x[0]*x[0]+y[0]*y[0]);
        y[3]=x[0]*(x[2]*x[2]+y[2]*y[2])+x[1]*(x[0]*x[0]+y[0]*y[0])+x[2]*(x[1]*x[1]+y[1]*y[1])-x[0]*(x[1]*x[1]+y[1]*y[1])-x[1]*(x[2]*x[2]+y[2]*y[2])-x[2]*(x[0]*x[0]+y[0]*y[0]);
        y[3]=-y[3];
        x[3]/=x[0]*y[1]+x[1]*y[2]+x[2]*y[0]-x[0]*y[2]-x[1]*y[0]-x[2]*y[1];
        y[3]/=x[0]*y[1]+x[1]*y[2]+x[2]*y[0]-x[0]*y[2]-x[1]*y[0]-x[2]*y[1];
        y[3]/=2;
        x[3]/=2;
        printf("%.3f %.3f %.3f\n",x[3],y[3],sqrt((x[3]-x[0])*(x[3]-x[0])+(y[3]-y[0])*(y[3]-y[0])));
    }
}